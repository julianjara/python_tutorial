# to modify items on a list 
# list[index of item to be modified] = new_value
list=[24, 23, 27, 40, 12, 60, 30]
list[1] = 88
print(list[1])

# to add items you use the append() function
# the following will add '99' to the end of the list
list.append(99)
print(list)

# to delete use the followin command 'del'
del list[1]
print(list)